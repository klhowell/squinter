use crate::serde_le::error::{Result, Error};

pub struct Deserializer<'de> {
    reader: &'de mut dyn Read
}

pub fn from_reader<'a, A, T>(r: &'a mut A) -> io::Result<T>
where
    A: Read,
    T: Deserialize<'a>,
{
    let mut des = Deserializer::from_reader(r);
    let t = T::deserialize(&mut des)?;
    Ok(t)
}

impl<'de> Deserializer<'de> {
    pub fn from_bytes(b: &'de [u8]) {}

    pub fn from_reader<A>(r: &'de mut A) -> Self
    where A: Read
    {
        Deserializer { reader: r }
    }
}

